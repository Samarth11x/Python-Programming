# List (mutable)
my_list = [1, 2, 3]
my_list[0] = 10
print("Modified list:", my_list)

# Tuple (immutable)
my_tuple = (1, 2, 3)
# my_tuple[0] = 10  # Uncommenting this will raise TypeError

# Set (mutable)
my_set = {1, 2, 3}
my_set.add(4)
print("Modified set:", my_set)

# Dictionary (mutable)
my_dict = {"a": 1, "b": 2}
my_dict["a"] = 10
print("Modified dictionary:", my_dict)

