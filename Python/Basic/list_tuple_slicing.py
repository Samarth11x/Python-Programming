# Sample list and tuple
my_list = [10, 20, 30, 40, 50, 60]
my_tuple = (10, 20, 30, 40, 50, 60)

# Slicing operations
print("List - First 3 elements:", my_list[:3])
print("List - Last 3 elements:", my_list[-3:])
print("List - Every second element:", my_list[::2])

print("Tuple - First 3 elements:", my_tuple[:3])
print("Tuple - Last 3 elements:", my_tuple[-3:])
print("Tuple - Every second element:", my_tuple[::2])
